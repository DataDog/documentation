#!/usr/bin/env python3
"""
Insert shortcode references for generated feature parity snippets.

This script consumes files generated by:
  local/bin/py/read_configs_from_feature_parity.py --generate-docs

Rules implemented:
  - Platform "multi" products are referenced per language in:
      content/en/tracing/trace_collection/library_config/<language>.md
  - Platform "" products are merged across languages first, deduplicated by key,
    written to a shared generated file, then referenced from a single confirmed
    target page.
  - Conflicting merged entries emit warnings; the winner is the last language
    processed.
  - Insertion point is above the "## Further Reading" (or "## Further reading")
    heading.
  - Idempotent updates via managed markers.
"""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path

from read_configs_from_feature_parity import COLLAPSED_TRACE_INTEGRATION_KEY, PRODUCTS

AUTOGEN_START = "<!-- FEATURE_PARITY_AUTOGEN_START -->"
AUTOGEN_END = "<!-- FEATURE_PARITY_AUTOGEN_END -->"
FURTHER_READING_PATTERN = re.compile(r"^## Further [Rr]eading\s*$", re.MULTILINE)
NEXT_STEPS_PATTERN = re.compile(r"^## Next steps\s*$", re.MULTILINE)
ENTRY_BLOCK_PATTERN = re.compile(
    r"^`[^`\n]+`\n: .*?(?=^\n?`[^`\n]+`\n: |^###\s|^##\s|^#\s|\Z)",
    re.MULTILINE | re.DOTALL,
)
H1_TO_H6_HEADING_PATTERN = re.compile(r"^(?P<hashes>#{1,6})\s(?P<title>.*)$", re.MULTILINE)

# Confirmed by user during interactive selection.
MERGED_PRODUCT_TARGETS = {
    "ai_guard": "content/en/security/ai_guard/onboarding.md",
    "application_security": "content/en/security/application_security/_index.md",
    "data_streams": "content/en/data_streams/manual_instrumentation.md",
    "dynamic_instrumentation": "content/en/tracing/trace_collection/dynamic_instrumentation/_index.md",
    "error_tracking": "content/en/error_tracking/backend/exception_replay.md",
    "code_security": "content/en/security/code_security/iast/setup/nodejs.md",
    "opentelemetry": "content/en/opentelemetry/config/environment_variable_support.md",
    "remote_configuration": "content/en/remote_configuration/_index.md",
    "runtime_metrics": "content/en/tracing/metrics/runtime_metrics/_index.md",
    "tests": "content/en/tests/_index.md",
}

LANGUAGE_SPECIFIC_PRODUCT_TARGETS = {
    "profiler": {
        "dotnet-core": "content/en/profiler/profiler_troubleshooting/dotnet.md",
        "go": "content/en/profiler/profiler_troubleshooting/go.md",
        "java": "content/en/profiler/profiler_troubleshooting/java.md",
        "nodejs": "content/en/profiler/profiler_troubleshooting/nodejs.md",
        "php": "content/en/profiler/profiler_troubleshooting/php.md",
        "python": "content/en/profiler/profiler_troubleshooting/python.md",
        "ruby": "content/en/profiler/profiler_troubleshooting/ruby.md",
    },
}

LANGUAGE_ORDER = [
    "cpp",
    "dotnet-core",
    "dotnet-framework",
    "go",
    "java",
    "nodejs",
    "php",
    "python",
    "ruby",
    "rust",
]

SHARED_MERGED_DIRECTORY = "_shared"
OTHER_CATEGORY = ("other", "Other")
COLLAPSE_INCLUDE_TEMPLATE = """{{{{% collapse-content title="{title}" level="h4" expanded=false id="{collapse_id}" %}}}}
{{{{< include-markdown "{include_ref}" >}}}}
{{{{% /collapse-content %}}}}"""


def product_definitions() -> list[tuple[str, dict]]:
    return [(prefix, data) for prefix, data in PRODUCTS.items() if not data.get("skip")]


def dedupe_multi_products(products: list[tuple[str, str]]) -> list[tuple[str, str]]:
    """
    Remove duplicate multi-product categories while preserving order.

    Duplicates can happen when multiple env var prefixes map to the same target
    product page/category.
    """
    seen_names: set[str] = set()
    deduped = []
    for product_name, display_name in products:
        if product_name in seen_names:
            continue
        seen_names.add(product_name)
        deduped.append((product_name, display_name))
    return deduped


def language_directories(generated_root: Path) -> list[str]:
    dirs = [p.name for p in generated_root.iterdir() if p.is_dir() and not p.name.startswith("_")]
    order_index = {name: idx for idx, name in enumerate(LANGUAGE_ORDER)}
    return sorted(dirs, key=lambda name: (order_index.get(name, 10_000), name))


def parse_generated_blocks(content: str) -> tuple[str | None, dict[str, str]]:
    """
    Parse generated per-product markdown into:
      - heading line (for example "### Tracing")
      - mapping of env var key -> full block
    """
    stripped = content.strip()
    if not stripped:
        return None, {}

    lines = stripped.splitlines()
    heading = None
    for line in lines:
        if line.strip():
            heading = line.strip()
            break

    if not heading or not heading.startswith("### "):
        return None, {}

    body = stripped[len(heading) :].lstrip()
    pattern = re.compile(
        r"^`(?P<label>[^`]+)`\n: .*?(?=^`[^`]+`\n: |\Z)",
        re.MULTILINE | re.DOTALL,
    )

    blocks: dict[str, str] = {}
    for match in pattern.finditer(body):
        label = match.group("label")
        key = label.removesuffix(" (Deprecated)")
        blocks[key] = match.group(0).strip()
    return heading, blocks


def parse_env_keys_from_generated_block(block: str) -> set[str]:
    """
    Parse env var keys from generated snippet blocks.

    This also captures original-key lists embedded in aggregated descriptions.
    """
    keys: set[str] = set()
    for match in re.finditer(r"`([A-Z][A-Z0-9_<>]+)`", block):
        keys.add(match.group(1))
    return keys


def parse_collapsed_trace_instrumentation_keys(block: str) -> set[str]:
    """
    Parse instrumentation bullet list from collapsed trace integration block.
    """
    reconstructed: set[str] = set()
    for line in block.splitlines():
        stripped = line.strip()
        if not stripped.startswith("- "):
            continue
        token = stripped[2:].strip()
        if not re.fullmatch(r"[A-Z0-9_]+", token):
            continue
        reconstructed.add(f"DD_TRACE_{token}_ENABLED")
    return reconstructed


def collect_env_keys_from_generated_blocks(generated_blocks: dict[str, str]) -> set[str]:
    keys: set[str] = set(generated_blocks.keys())
    for generated_key, block in generated_blocks.items():
        keys.update(parse_env_keys_from_generated_block(block))
        if generated_key == COLLAPSED_TRACE_INTEGRATION_KEY:
            keys.update(parse_collapsed_trace_instrumentation_keys(block))
    return keys


def parse_env_keys_from_entry_block(block: str) -> set[str]:
    """
    Parse env var keys represented by an entry block.

    Handles both:
      - env var as title (`DD_TRACE_ENABLED`)
      - programmatic title with env var in the body
        (`dd.trace.enabled` + **Environment Variable**: `DD_TRACE_ENABLED`)
    """
    keys: set[str] = set()
    lines = block.splitlines()
    if lines:
        title_line = lines[0].strip()
        if title_line.startswith("`") and title_line.endswith("`"):
            title_token = title_line[1:-1].strip()
            if re.fullmatch(r"[A-Z][A-Z0-9_<>]+", title_token):
                keys.add(title_token)

    for line in lines:
        if "Environment Variable" not in line:
            continue
        for match in re.finditer(r"`([A-Z][A-Z0-9_<>]+)`", line):
            keys.add(match.group(1))

    return keys


def remove_duplicate_entries_and_empty_categories(
    content: str, duplicate_env_keys: set[str]
) -> str:
    """
    Remove duplicated entry blocks and prune empty H3 categories.

    A category section is removed when:
      - it originally contained at least one entry block
      - all its entry blocks were removed as duplicates
    """
    if not duplicate_env_keys:
        return content

    # Pass 1: remove duplicate entry blocks anywhere in the page body.
    entry_matches = list(ENTRY_BLOCK_PATTERN.finditer(content))
    if not entry_matches:
        cleaned = content
    else:
        parts = []
        cursor = 0
        for match in entry_matches:
            block = match.group(0)
            keys = parse_env_keys_from_entry_block(block)
            if keys & duplicate_env_keys:
                parts.append(content[cursor:match.start()])
                cursor = match.end()
        parts.append(content[cursor:])
        cleaned = "".join(parts)
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned)

    # Pass 2: prune now-empty H2/H3 sections iteratively.
    #
    # A section is considered empty when it has no body content after duplicate
    # removal (for example a heading followed directly by another heading).
    pruned = cleaned
    while True:
        heading_matches = list(H1_TO_H6_HEADING_PATTERN.finditer(pruned))
        removable_range: tuple[int, int] | None = None

        for idx, heading_match in enumerate(heading_matches):
            level = len(heading_match.group("hashes"))
            title = heading_match.group("title").strip()
            if level not in (2, 3):
                continue
            if re.fullmatch(r"Further [Rr]eading", title):
                continue
            if title == "Next steps":
                continue

            section_start = heading_match.start()
            section_end = len(pruned)
            for next_heading in heading_matches[idx + 1 :]:
                next_level = len(next_heading.group("hashes"))
                if next_level <= level:
                    section_end = next_heading.start()
                    break

            section_body = pruned[heading_match.end() : section_end]
            if section_body.strip():
                continue

            removable_range = (section_start, section_end)
            break

        if not removable_range:
            break

        start, end = removable_range
        pruned = f"{pruned[:start]}{pruned[end:]}"
        pruned = re.sub(r"\n{3,}", "\n\n", pruned)

    return pruned


def strip_existing_autogen_block(content: str) -> str:
    escaped_start = re.escape(AUTOGEN_START)
    escaped_end = re.escape(AUTOGEN_END)
    pattern = re.compile(
        rf"{escaped_start}\n.*?\n{escaped_end}",
        re.DOTALL,
    )
    return pattern.sub("", content)


def page_ref_from_content_file(content_path: Path, repo_root: Path) -> str:
    """
    Convert content file path to include-markdown page reference.

    Example:
      content/en/tracing/trace_collection/library_config/nodejs/tracing.md
      -> tracing/trace_collection/library_config/nodejs/tracing
    """
    relative = content_path.relative_to(repo_root / "content" / "en")
    return relative.with_suffix("").as_posix()


def render_include_markdown_ref(content_path: Path, repo_root: Path) -> str:
    return f'{{{{< include-markdown "{page_ref_from_content_file(content_path, repo_root)}" >}}}}'


def upsert_into_target(
    target_path: Path,
    generated_section: str,
    dry_run: bool,
    duplicate_env_keys: set[str] | None = None,
) -> tuple[bool, str | None]:
    """Insert or replace generated section above Further Reading anchor."""
    if not target_path.exists():
        return False, f"missing target file: {target_path}"

    original = target_path.read_text(encoding="utf-8")
    without_autogen = strip_existing_autogen_block(original)
    if duplicate_env_keys:
        without_autogen = remove_duplicate_entries_and_empty_categories(
            without_autogen, duplicate_env_keys
        )
    anchor_match = FURTHER_READING_PATTERN.search(without_autogen)
    if not anchor_match:
        anchor_match = NEXT_STEPS_PATTERN.search(without_autogen)
    if not anchor_match:
        return False, f"no '## Further Reading' or '## Next steps' anchor in: {target_path}"

    block = f"{AUTOGEN_START}\n{generated_section.rstrip()}\n{AUTOGEN_END}"
    prefix = without_autogen[: anchor_match.start()].rstrip("\n")
    suffix = without_autogen[anchor_match.start() :].lstrip("\n")
    updated = f"{prefix}\n\n{block}\n\n{suffix}"

    if updated == original:
        return False, None

    if not dry_run:
        target_path.write_text(updated, encoding="utf-8")
    return True, None


def upsert_generated_file(path: Path, content: str, dry_run: bool) -> bool:
    """Write generated file only when content changes."""
    if path.exists():
        original = path.read_text(encoding="utf-8")
        if original == content:
            return False
    if not dry_run:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
    return True


def render_combined_section(product_files: list[Path]) -> tuple[str, list[str]]:
    """
    Merge product snippets from multiple language files.

    Returns:
      rendered markdown section
      conflict warnings
    """
    heading: str | None = None
    merged_blocks: dict[str, tuple[str, str]] = {}
    warnings: list[str] = []

    for source_path in product_files:
        language = source_path.parent.name
        source_heading, blocks = parse_generated_blocks(source_path.read_text(encoding="utf-8"))
        if not source_heading:
            continue

        if heading is None:
            heading = source_heading
        elif heading != source_heading:
            warnings.append(
                f"Heading mismatch for {source_path.name}: '{heading}' vs '{source_heading}'. Using '{source_heading}'."
            )
            heading = source_heading

        for key, block in blocks.items():
            if key in merged_blocks and merged_blocks[key][0] != block:
                previous_language = merged_blocks[key][1]
                warnings.append(
                    f"Conflict for {source_path.name} key '{key}': "
                    f"overriding {previous_language} with {language}."
                )
            merged_blocks[key] = (block, language)

    if not heading or not merged_blocks:
        return "", warnings

    lines = [heading, ""]
    for key in sorted(merged_blocks.keys()):
        lines.append(merged_blocks[key][0])
        lines.append("")
    return "\n".join(lines).rstrip() + "\n", warnings


def render_collapsible_include(title: str, collapse_id: str, include_ref: str) -> str:
    return COLLAPSE_INCLUDE_TEMPLATE.format(
        title=title,
        collapse_id=collapse_id,
        include_ref=include_ref,
    )


def load_language_alias_graph(repo_root: Path, language: str) -> dict[str, set[str]]:
    """
    Load alias relationships from local/bin/output/registry_<language>.json.

    The graph is bidirectional: canonical key <-> alias.
    """
    registry_json = repo_root / "local" / "bin" / "output" / f"registry_{language}.json"
    if not registry_json.exists():
        return {}

    try:
        payload = json.loads(registry_json.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}

    graph: dict[str, set[str]] = {}
    for row in payload.get("configurations", []):
        key = row.get("name")
        if not key:
            continue
        graph.setdefault(key, set())
        for alias in row.get("aliases") or []:
            if not alias:
                continue
            graph.setdefault(key, set()).add(alias)
            graph.setdefault(alias, set()).add(key)
    return graph


def expand_env_keys_with_aliases(keys: set[str], alias_graph: dict[str, set[str]]) -> set[str]:
    """
    Expand key set transitively through alias graph.
    """
    if not keys or not alias_graph:
        return keys

    expanded = set(keys)
    queue = list(keys)
    while queue:
        current = queue.pop()
        for neighbor in alias_graph.get(current, set()):
            if neighbor in expanded:
                continue
            expanded.add(neighbor)
            queue.append(neighbor)
    return expanded


def build_language_multi_reference_section(
    generated_root: Path,
    language: str,
    multi_products_in_order: list[tuple[str, str]],
    repo_root: Path,
    alias_graph: dict[str, set[str]],
) -> tuple[str, set[str]]:
    refs = []
    seen_include_refs: set[str] = set()
    seen_category_titles: set[str] = set()
    included_env_keys: set[str] = set()
    for product_name, product_display_name in multi_products_in_order:
        product_file = generated_root / language / f"{product_name}.md"
        if not product_file.exists():
            continue
        include_ref = page_ref_from_content_file(product_file, repo_root)
        if include_ref in seen_include_refs:
            continue

        # Also dedupe same category title in one language overview page.
        if product_display_name in seen_category_titles:
            continue

        seen_include_refs.add(include_ref)
        seen_category_titles.add(product_display_name)
        collapse_id = f"feature-parity-{language}-{product_name}"
        refs.append(render_collapsible_include(product_display_name, collapse_id, include_ref))

        _, generated_blocks = parse_generated_blocks(product_file.read_text(encoding="utf-8"))
        included_env_keys.update(collect_env_keys_from_generated_blocks(generated_blocks))

    if not refs:
        return "", set()

    # Also remove duplicates for keys represented in non-included generated files
    # (for example platform-independent products generated per language).
    language_dir = generated_root / language
    for product_file in sorted(language_dir.glob("*.md")):
        _, generated_blocks = parse_generated_blocks(product_file.read_text(encoding="utf-8"))
        included_env_keys.update(collect_env_keys_from_generated_blocks(generated_blocks))

    included_env_keys = expand_env_keys_with_aliases(included_env_keys, alias_graph)
    return "\n\n".join(refs).rstrip() + "\n", included_env_keys


def main() -> None:
    parser = argparse.ArgumentParser(description="Inline generated feature parity config snippets into docs pages.")
    parser.add_argument(
        "--generated-root",
        default="content/en/tracing/trace_collection/library_config",
        help="Root directory containing generated language subfolders.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would change without writing files.",
    )
    parser.add_argument(
        "--shared-dir-name",
        default=SHARED_MERGED_DIRECTORY,
        help="Directory name under generated root for merged platform-independent snippets.",
    )
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[3]
    generated_root = (repo_root / args.generated_root).resolve()
    if not generated_root.exists():
        raise SystemExit(f"generated root does not exist: {generated_root}")

    languages = language_directories(generated_root)
    if not languages:
        raise SystemExit(f"no language directories found under: {generated_root}")

    products = product_definitions()
    multi_products = dedupe_multi_products(
        [
            (data["name"], data["displayName"])
            for _, data in products
            if data.get("platform") == "multi"
        ]
    )
    multi_products.append(OTHER_CATEGORY)
    updated_files: list[Path] = []
    warnings: list[str] = []

    # Platform-specific (multi): inline per language into library config pages.
    for language in languages:
        target = generated_root / f"{language}.md"
        alias_graph = load_language_alias_graph(repo_root, language)
        section, included_env_keys = build_language_multi_reference_section(
            generated_root, language, multi_products, repo_root, alias_graph
        )
        if not section:
            continue
        changed, warning = upsert_into_target(
            target,
            section,
            args.dry_run,
            duplicate_env_keys=included_env_keys,
        )
        if warning:
            warnings.append(warning)
        if changed:
            updated_files.append(target)

    # Product-level merged targets (single target per product).
    for product_name, target_rel_path in MERGED_PRODUCT_TARGETS.items():
        source_files = []
        for language in languages:
            product_file = generated_root / language / f"{product_name}.md"
            if product_file.exists():
                source_files.append(product_file)

        if not source_files:
            continue

        merged_section, merge_warnings = render_combined_section(source_files)
        warnings.extend(merge_warnings)
        if not merged_section:
            continue

        merged_file = generated_root / args.shared_dir_name / f"{product_name}.md"
        if upsert_generated_file(merged_file, merged_section, args.dry_run):
            updated_files.append(merged_file)

        reference_section = render_include_markdown_ref(merged_file, repo_root)
        target = repo_root / target_rel_path
        changed, warning = upsert_into_target(target, reference_section, args.dry_run)
        if warning:
            warnings.append(warning)
        if changed:
            updated_files.append(target)

    # Product targets that are language-specific files.
    for product_name, language_targets in LANGUAGE_SPECIFIC_PRODUCT_TARGETS.items():
        for language, target_rel_path in language_targets.items():
            product_file = generated_root / language / f"{product_name}.md"
            if not product_file.exists():
                continue

            include_section = render_include_markdown_ref(product_file, repo_root)
            _, generated_blocks = parse_generated_blocks(product_file.read_text(encoding="utf-8"))
            duplicate_env_keys = collect_env_keys_from_generated_blocks(generated_blocks)
            alias_graph = load_language_alias_graph(repo_root, language)
            duplicate_env_keys = expand_env_keys_with_aliases(duplicate_env_keys, alias_graph)

            target = repo_root / target_rel_path
            changed, warning = upsert_into_target(
                target,
                include_section,
                args.dry_run,
                duplicate_env_keys=duplicate_env_keys,
            )
            if warning:
                warnings.append(warning)
            if changed:
                updated_files.append(target)

    mode = "Would update" if args.dry_run else "Updated"
    print(f"{mode} {len(updated_files)} files.")
    for path in sorted(set(updated_files)):
        print(f"  - {path}")

    if warnings:
        print("\nWarnings:")
        for warning in warnings:
            print(f"  - {warning}")


if __name__ == "__main__":
    main()
